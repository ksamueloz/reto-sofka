    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        if (JOptionPane.showConfirmDialog(null,
                                   "Seguro que quiere eliminar esta nave?", "SALIR",
                                 JOptionPane.YES_NO_CANCEL_OPTION) == 0) {
            try {
                Shuttle lanz = new Shuttle();
                lanz.setId(Integer.parseInt(txtId.getText()));
                
                // Se elimina la nave.
                if (lanz.deleteShip(lanz.getId())) {
                    JOptionPane.showMessageDialog(rootPane, "Eliminado con exito.");
                    showDataShuttle();
                    clearData();
                } else {
                    System.out.println("No se ha seleccionado la fila.");
                }
            } catch (Exception e) {
                System.out.println("Error con eliminar: " + e);
            }
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

