    private void jButtonRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRegisterActionPerformed

        try {
            Shuttle lanz = new Shuttle();
            // Atributos generales de la nave.
            lanz.setName(txtNameLanz.getText());
            lanz.setSpeed(Double.parseDouble(txtSpeedLanz.getText()));
            lanz.setWeight(Integer.parseInt(txtWeightLanz.getText()));
            lanz.setPropulsionSystem(txtPropulsionSysLanz.getText());
            lanz.setTonThrust(Integer.parseInt(txtTonThrustLanz.getText()));
            lanz.setFuel(txtFuelLanz.getText());
            lanz.setDescription(txtDescriptionLanz.getText());

            // Atributos específicos del tipo Lanzadera.
            lanz.setTransportCapacity(txtCapacityLanz.getText());
            lanz.setHeight(Double.parseDouble(txtHeightLanz.getText()));
            lanz.setPower(Double.parseDouble(txtPowerLanz.getText()));

            // Se crea la nave tipo lanzadera.
            if(lanz.createShip()) {
                // Mostrar datos.
                showDataShuttle();
                clearData();
            }

        } catch (Exception e) {
            System.out.println("La inserción falló, revise que ningún campo esté vacío.");
        }
    }//GEN-LAST:event_jButtonRegisterActionPerformed

