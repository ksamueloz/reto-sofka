    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        if (JOptionPane.showConfirmDialog(null,
                                   "Seguro que quiere eliminar esta nave?", "SALIR",
                                 JOptionPane.YES_NO_CANCEL_OPTION) == 0) {
            try {
                Unmanned unm = new Unmanned();
                unm.setId(Integer.parseInt(txtIdNoTripulada.getText()));
                
                // Se elimina la nave.
                if (unm.deleteShip(unm.getId())) {
                    JOptionPane.showMessageDialog(rootPane, "Eliminado con exito.");
                    showDataUnManned();
                    clearData();
                } else {
                    System.out.println("No se ha seleccionado la fila.");
                }
            } catch (Exception e) {
                System.out.println("Error con eliminar: " + e);
            }
        }
    }//GEN-LAST:event_jMenuItem2ActionPerformed

